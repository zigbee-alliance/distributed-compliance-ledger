#!/usr/bin/env bash
#
# Patches every ts-client/*/rest.ts so axios percent-encodes query
# parameter values. Without this, base64 pagination keys (which contain
# '+', '/', '=') are mangled in transit -- '+' is decoded as space on
# the server, producing "illegal base64 data" errors on next_key follow-up
# requests. Idempotent: re-run safely after `ignite generate ts-client`.

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
TS_CLIENT_DIR="$REPO_ROOT/ts-client"

if [[ ! -d "$TS_CLIENT_DIR" ]]; then
  echo "error: $TS_CLIENT_DIR not found" >&2
  exit 1
fi

# Canonical paramsSerializer module. Overwritten every run so the function
# lives in exactly one place; self-heals if `ignite generate ts-client` wiped it.
cat > "$TS_CLIENT_DIR/utils.ts" <<'EOF'
// Generated by scripts/patch-ts-client-encoding.sh. Do not edit by hand.
export function paramsSerializer(params: Record<string, any>): string {
  const parts: string[] = [];
  for (const key of Object.keys(params)) {
    const value = params[key];
    if (value === undefined || value === null) continue;
    const ek = encodeURIComponent(key);
    if (Array.isArray(value)) {
      for (const item of value) parts.push(`${ek}=${encodeURIComponent(String(item))}`);
    } else {
      parts.push(`${ek}=${encodeURIComponent(String(value))}`);
    }
  }
  return parts.join("&");
}
EOF

OLD_LINE='    this.instance = axios.create({ ...axiosConfig, baseURL: axiosConfig.baseURL || "" });'

IFS= read -r -d '' NEW_BLOCK <<'EOF' || true
    this.instance = axios.create({
      ...axiosConfig,
      baseURL: axiosConfig.baseURL || "",
      // RFC 3986 encode every value so base64 pagination keys (+, /, =) survive transit.
      paramsSerializer: paramsSerializer,
    });
EOF

patched=0
skipped=0
missing=0

while IFS= read -r -d '' file; do
  # Accept either `paramsSerializer,` (shorthand) or `paramsSerializer: ...,`.
  has_block=no; grep -qE "^[[:space:]]+paramsSerializer[,:]" "$file" && has_block=yes
  has_import=no; grep -qF 'from "../utils"' "$file" && has_import=yes

  if [[ "$has_block" == yes && "$has_import" == yes ]]; then
    skipped=$((skipped + 1))
    continue
  fi
  if [[ "$has_block" == no ]] && ! grep -qF "$OLD_LINE" "$file"; then
    echo "warn: anchor line not found in $file" >&2
    missing=$((missing + 1))
    continue
  fi
  python3 - "$file" "$OLD_LINE" "$NEW_BLOCK" "$has_block" "$has_import" <<'PY'
import sys, pathlib
path = pathlib.Path(sys.argv[1])
old_line, new_block = sys.argv[2], sys.argv[3]
has_block, has_import = sys.argv[4] == "yes", sys.argv[5] == "yes"
AXIOS = 'import axios, { AxiosInstance, AxiosRequestConfig, AxiosResponse, ResponseType } from "axios";'
IMPORT = 'import { paramsSerializer } from "../utils";'
text = path.read_text()
if not has_block:
    if old_line not in text:
        sys.exit(f"anchor missing in {path}")
    text = text.replace(old_line, new_block, 1)
if not has_import:
    if AXIOS not in text:
        sys.exit(f"axios import anchor missing in {path}")
    text = text.replace(AXIOS, AXIOS + "\n" + IMPORT, 1)
path.write_text(text)
PY
  patched=$((patched + 1))
  echo "patched $file"
done < <(find "$TS_CLIENT_DIR" -type f -name "rest.ts" -print0)

echo "done. patched=$patched skipped=$skipped missing=$missing"

if [[ $missing -gt 0 ]]; then
  exit 2
fi
